def api_post(path, payload):
    data = json.dumps(payload, ensure_ascii=False).encode("utf-8")
    req = urllib.request.Request(
        f"{BASE}{path}", data=data,
        headers={"Content-Type": "application/json; charset=utf-8"},
        method="POST",
    )
    with urllib.request.urlopen(req) as resp:
        return json.loads(resp.read().decode("utf-8"))


def api_get(path):
    req = urllib.request.Request(f"{BASE}{path}")
    with urllib.request.urlopen(req) as resp:
        return json.loads(resp.read().decode("utf-8"))


def main():
    # 查询已有知识标题，按 title 去重
    existing = api_get("/knowledge?page=1&page_size=200")
    existing_titles = {k["title"] for k in existing["data"].get("items", [])}

    created = 0
    skipped = 0
    for kdef in DEMO_KNOWLEDGE:
        if kdef["title"] in existing_titles:
            print(f"[skip] 已存在: {kdef['title']}")
            skipped += 1
            continue
        payload = {
            "title": kdef["title"],
            "content": kdef["content"],
            "content_type": "markdown",
            "category_id": kdef.get("category_id"),
            "tag_ids": kdef.get("tag_ids", []),
            "status": "published",
        }
        res = api_post("/knowledge", payload)
        kid = res.get("data", {}).get("id")
        print(f"[ok] ID={kid} {kdef['title']} (cat={kdef.get('category_id')}, tags={kdef.get('tag_ids')})")
        created += 1

    print(f"\n完成：新增 {created} 条，跳过 {skipped} 条")


if __name__ == "__main__":
    main()
